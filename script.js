// InterattivitÃ  con bottoni e contenuti
document.addEventListener("DOMContentLoaded", () => {
    const bottoni = document.querySelectorAll(".bottone-indirizzo");

    bottoni.forEach(bottone => {
        bottone.addEventListener("click", () => {
            bottone.style.backgroundColor = "#3CB371";
        });
    });

    // Centrare automaticamente le tabelle su schermi piccoli
    const tabelle = document.querySelectorAll("table");
    tabelle.forEach(tabella => {
        tabella.style.marginLeft = "auto";
        tabella.style.marginRight = "auto";
    });
});

document.addEventListener("DOMContentLoaded", () => {
    const body = document.body;
    body.style.minHeight = `${window.innerHeight}px`; // Adatta il contenuto alla finestra
});
